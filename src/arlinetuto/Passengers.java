int PassId=0;
private void CountPassengers() {
    try {
        // Kiểm tra xem kết nối đã được mở chưa, nếu chưa thì mở lại
        if (Con == null || Con.isClosed()) {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedb", "root", "12345");
        }
        
        Stl = Con.createStatement();
        Rsl = Stl.executeQuery("SELECT MAX(pid) FROM PassengersTbl");
        if (Rsl.next()) {
            PassId = Rsl.getInt(1);
            if (PassId == 0) {
                PassId = 1; // Nếu không có bản ghi nào trong bảng, bắt đầu từ 1
            } else {
                PassId++; // Nếu có bản ghi, tăng PassId lên 1
            }
        } else {
            PassId = 1; // Nếu không có bản ghi nào trong bảng, bắt đầu từ 1
        }
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi đếm số hành khách: " + e.getMessage());
    }
}


