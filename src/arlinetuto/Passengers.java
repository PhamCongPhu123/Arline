    private void SaveDtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveDtnMouseClicked
      if (PNameTb.getText().isEmpty() || PassNumTb.getText().isEmpty() || PAddresTb.getText().isEmpty() || PPhoneTb.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Missing Information");
} else {
    try {
        // Kiểm tra số điện thoại nhập liệu có chứa toàn ký tự số không
        String phoneNumber = PPhoneTb.getText();
        if (!phoneNumber.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Invalid Phone Number");
            return; // Không thêm bản ghi nếu số điện thoại không hợp lệ
        }

        // Tiến hành thêm bản ghi
        CountPassengers();
        try (Connection Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedb", "root", "12345")) {
            PreparedStatement Add = Con.prepareStatement("INSERT INTO PassengersTbl VALUES (?, ?, ?, ?, ?, ?, ?)");
            Add.setInt(1, PassId);
            Add.setString(2, PNameTb.getText());
            Add.setString(3, NatCb.getSelectedItem().toString());
            Add.setString(4, GEnCb.getSelectedItem().toString());
            Add.setString(5, PassNumTb.getText());
            Add.setString(6, PAddresTb.getText());
            Add.setString(7, PPhoneTb.getText());

            int row = Add.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Passenger Added");
                DisplayPassengers();
                Clear();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add passenger");
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

   
    }//GEN-LAST:event_SaveDtnMouseClicked

